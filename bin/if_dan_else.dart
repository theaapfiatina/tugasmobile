void main() {
  var nilai = 70;

  print('nilai = $nilai');

  if (nilai == 80) {
    print('Nilai Anda Benar 70');
  }
  print('selesai\n');

  /**Else */
  var nilai1 = 70;

  print('nilai1 = $nilai');

  if (nilai1 == 80) {
    print('Nilai Anda Benar 70');
  } else {
    print('Nilai Anda Bukan 70');
  }
  print('selesai\n');

  /**Else If */
  var nilai2 = 80;
  print("Ini adalah awal Dari Program");

  if (nilai2 == 70) {
    print("Anda Mendapatkan Nilai 70");
  } else if (nilai2 == 80) {
    print("Anda Mendapatkan Nilai 80");
  } else {
    print("Nilai Anda Bukan 70 atau 80");
  }
  print("Ini Akhir Dari Program");
}
